// Regenerates packages/attest-core/golden/canonicalize-golden-vectors.json — the SHARED canonicalize()
// cross-runtime contract. Every runtime (Node / browser / C#) must reproduce canonical_text, utf8_len,
// utf8_hex, sha256_hex EXACTLY. Values here are deliberately broader than a real v2 envelope (ASCII-only)
// to prove the C# mirror matches JS on the full Unicode/escaping/ordering matrix — where a naive
// serializer (e.g. System.Text.Json, which escapes U+2028/U+2029 and non-ASCII) would drift.
//   node scripts/gen-canonicalize-golden.mjs
import { canonicalize } from "../packages/attest-core/attestation.js";
import { createHash } from "node:crypto";
import { writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const CC = String.fromCharCode;
const cases = {
  ascii_english: { note: "English ASCII", input: { verdict: "APPROVE", model: "claude-opus-4-8" } },
  simplified_chinese: { note: "Simplified Chinese literal (JSON.stringify does NOT escape non-ASCII)", input: { "说明": "贷款已批准", "备注": "符合政策" } },
  emoji_surrogate_pair: { note: "emoji = valid UTF-16 surrogate pair, output literally", input: { flag: "🚩", ok: "✅🎉" } },
  json_null: { note: "explicit null distinct from absent", input: { previous_hash: null } },
  absent_vs_present: { note: "object WITHOUT the optional key (absent) — different bytes from null", input: { a: "1" } },
  nested_key_ordering: { note: "nested object keys sorted independently", input: { z: { b: "2", a: "1" }, a: { d: "4", c: "3" } } },
  escaped_quote: { note: "double quote inside a value", input: { s: 'he said "hi"' } },
  backslash: { note: "backslash", input: { path: "a\\b\\c" } },
  newline: { note: "newline -> backslash-n", input: { s: "line1" + CC(0x0a) + "line2" } },
  tab: { note: "tab -> backslash-t", input: { s: "a" + CC(0x09) + "b" } },
  u2028_line_sep: { note: "U+2028 passes through literally (JSON.stringify does NOT escape it)", input: { s: "a" + CC(0x2028) + "b" } },
  u2029_para_sep: { note: "U+2029 passes through literally", input: { s: "a" + CC(0x2029) + "b" } },
  control_chars: { note: "assorted control chars -> escapes", input: { s: CC(0x01) + CC(0x1f) + CC(0x0d) } },
  array_order_preserved: { note: "arrays keep order (not sorted)", input: { xs: ["c", "a", "b"] } },
  bool_values: { note: "booleans", input: { yes: true, no: false } },
  integer_values: { note: "integers", input: { n: 0, big: 1234567890 } },
  top_level_string: { note: "top-level non-object primitive", input: "hello" },
  top_level_null: { note: "top-level null", input: null },
  v2_envelope_shape: { note: "a real v2 envelope (ASCII-only)", input: { domain: "shadow-attestation", wire_version: "aex-attestation/v2", algorithm: "hmac-sha256", request_commitment: "1".repeat(64), output_commitment: "2".repeat(64), model_id: "claude", completed_at_utc: "2026-01-02T03:04:05.000Z", previous_hash: null, key_id: "k", bindings: { dictionary_hash: "a".repeat(64), citation_registry_sha256: "b".repeat(64) } } },
};

const vectors = {};
for (const [k, c] of Object.entries(cases)) {
  const text = canonicalize(c.input);
  const bytes = Buffer.from(text, "utf8");
  vectors[k] = { note: c.note, input: c.input, canonical_text: text, utf8_len: bytes.length, utf8_hex: bytes.toString("hex"), sha256_hex: createHash("sha256").update(bytes).digest("hex") };
}
const out = join(dirname(fileURLToPath(import.meta.url)), "..", "packages", "attest-core", "golden", "canonicalize-golden-vectors.json");
writeFileSync(out, JSON.stringify({ _comment: "SHARED canonicalize() cross-runtime golden vectors. Every runtime (Node/browser/C#) must reproduce canonical_text, utf8_len, utf8_hex, sha256_hex EXACTLY. Generated by scripts/gen-canonicalize-golden.mjs.", vectors }, null, 2) + "\n");
console.log("wrote", Object.keys(vectors).length, "vectors to", out);
