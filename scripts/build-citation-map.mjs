#!/usr/bin/env node
// scripts/build-citation-map.mjs
//
// Turn docs/CITATION_MAP.md (the human procurement doc) into a machine-readable
// docs/citation-map.json — the moat as an installable/queryable artifact, not a
// PDF. Every row of Section 2's tables becomes one entry:
//   { section, section_title, citation, full_name, personas[], test_files[],
//     other_refs[], test_count_raw }
//
// The .md stays the source of truth; this regenerates the .json (and the JSON is
// what tests + the query CLI + any MCP tool read). A row whose test file no
// longer exists is caught by test/citation-map.test.js — a citation map that
// rots is worse than none.
//
//   node scripts/build-citation-map.mjs           # writes docs/citation-map.json
//   node scripts/build-citation-map.mjs --check    # exit 1 if on-disk json is stale
import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, resolve } from "node:path";

const ROOT = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const MD = resolve(ROOT, "docs/CITATION_MAP.md");
const OUT = resolve(ROOT, "docs/citation-map.json");

// Canonical personas (Section 1) + the aliases used in table cells.
export const PERSONAS = [
  "Credit Fundamentals", "Risk Officer", "Fair Lending Compliance",
  "Customer Advocate", "Macro Contrarian", "AML/KYC Investigator",
];
const PERSONA_ALIAS = {
  "fair lending": "Fair Lending Compliance", "fair lending compliance": "Fair Lending Compliance",
  "credit": "Credit Fundamentals", "credit fundamentals": "Credit Fundamentals",
  "risk": "Risk Officer", "risk officer": "Risk Officer",
  "customer advocate": "Customer Advocate", "macro contrarian": "Macro Contrarian",
  "aml/kyc": "AML/KYC Investigator", "aml/kyc investigator": "AML/KYC Investigator",
};
// Non-persona labels that legitimately appear in the persona column.
export const AGGREGATE_LABELS = [
  "All 6", "All", "Persona-independent", "(all decisions)", "(context only)",
  "(advisor pack, roadmap)", "(none)", "(escalates all)",
];

const backticks = (s) => [...s.matchAll(/`([^`]+)`/g)].map((m) => m[1]);
const isRefPath = (t) => /^(test|lib|shadow-verify|docs)\//.test(t) && /\.(test\.)?(js|mjs|py|md)$/.test(t.split(":")[0]);

function parsePersonas(cell) {
  const trimmed = cell.trim();
  // a pure parenthetical label (e.g. "(advisor pack, roadmap)") is one token — don't split it
  if (/^\(.*\)$/.test(trimmed)) return [trimmed];
  // split on +, comma, slash — but keep "AML/KYC" together, so split slash only when spaced
  const parts = trimmed.split(/\s*\+\s*|,\s*/).flatMap((p) => p.split(/\s+\/\s+/)).map((p) => p.trim()).filter(Boolean);
  const out = [];
  for (const p of parts) {
    const canon = PERSONA_ALIAS[p.toLowerCase()];
    if (canon) out.push(canon);
    else if (AGGREGATE_LABELS.some((a) => p.toLowerCase().includes(a.toLowerCase().replace(/[()]/g, "")))) out.push(p);
    else out.push(p); // keep raw; validator decides if it's allowed
  }
  return [...new Set(out)];
}

export function buildFromMarkdown(md) {
  const lines = md.split("\n");
  const entries = [];
  let section = null, sectionTitle = null, inTable = false, headerSeen = false;
  for (const line of lines) {
    const h = line.match(/^###\s+(2\.[0-9.]+)\s+(.*)$/);
    if (h) { section = h[1]; sectionTitle = h[2].trim(); inTable = false; headerSeen = false; continue; }
    // any other header (## 3, ## 4, ### not-2.x) ends the Section-2 region
    if (/^#{1,3}\s/.test(line)) { section = null; inTable = false; headerSeen = false; continue; }
    if (!section) continue;
    if (line.trim().startsWith("|")) {
      const cells = line.split("|").slice(1, -1).map((c) => c.trim());
      if (/^\|?\s*Citation/i.test(line)) { headerSeen = true; inTable = true; continue; }
      if (/^\|[\s:-]+\|/.test(line) || cells.every((c) => /^:?-+:?$/.test(c))) continue; // separator
      if (!headerSeen || cells.length < 5) continue;
      const [citationRaw, fullName, personaCell, testCell, countCell] = cells;
      const citation = citationRaw.replace(/\*\*/g, "").trim();
      const refs = backticks(testCell).filter(isRefPath);
      const test_files = refs.filter((r) => r.startsWith("test/"));
      const other_refs = refs.filter((r) => !r.startsWith("test/"));
      entries.push({
        section, section_title: sectionTitle,
        citation, full_name: fullName.replace(/\*\*/g, "").trim(),
        personas: parsePersonas(personaCell),
        test_files, other_refs,
        test_count_raw: countCell.replace(/\*\*/g, "").trim(),
      });
    } else if (line.trim() === "" && inTable) { inTable = false; }
  }
  return {
    schema_version: 1,
    source: "docs/CITATION_MAP.md",
    note: "Generated by scripts/build-citation-map.mjs. Do not hand-edit; edit the .md and regenerate. Validated by test/citation-map.test.js (every test_file must exist; every persona must be real).",
    persona_catalog: PERSONAS,
    count: entries.length,
    entries,
  };
}

const md = readFileSync(MD, "utf8");
const data = buildFromMarkdown(md);
const json = JSON.stringify(data, null, 2) + "\n";

if (process.argv.includes("--check")) {
  let onDisk = "";
  try { onDisk = readFileSync(OUT, "utf8"); } catch {}
  if (onDisk !== json) {
    process.stderr.write("citation-map.json is stale — run `node scripts/build-citation-map.mjs` and commit.\n");
    process.exit(1);
  }
  process.stdout.write(`citation-map.json up to date (${data.count} entries).\n`);
} else {
  writeFileSync(OUT, json, "utf8");
  process.stdout.write(`wrote docs/citation-map.json — ${data.count} entries across Section 2 tables.\n`);
}
