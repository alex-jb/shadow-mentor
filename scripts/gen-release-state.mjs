#!/usr/bin/env node
// scripts/gen-release-state.mjs
// The SINGLE SOURCE OF TRUTH for Shadow's public-state numbers. For a product whose
// pitch is "trustworthy records", the same version/test/tool count must appear on
// every surface — README, SKILL.md, server.json, the MCP registry, demo badges.
// This computes them from source and writes release-state.json; run with --check in CI
// to fail on drift. Surfaces should be reconciled against THIS file, not each other.
import { execSync } from "node:child_process";
import { readFileSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const REPO = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const R = (...p) => resolve(REPO, ...p);
const readJSON = (p) => JSON.parse(readFileSync(R(p), "utf8"));

const rootPkg = readJSON("package.json");
const corePkg = readJSON("packages/attest-core/package.json");

// MCP tool count = the true number of tool handlers in the server.
const serverSrc = readFileSync(R("mcp/server.js"), "utf8");
const mcpTools = (serverSrc.match(/name === "shadow_/g) || []).length;

// Test totals: reuse the numbers readme-stats keeps fresh in the README block (CI
// blocks README drift, so this is authoritative without re-running the suite here).
const readme = readFileSync(R("README.md"), "utf8");
const testsMatch = readme.match(/\*\*Tests\*\*:\s*(\d+)\/(\d+)/);
const tests_passed = testsMatch ? Number(testsMatch[1]) : null;
const tests_total = testsMatch ? Number(testsMatch[2]) : null;

let commit = "";
try { commit = execSync("git rev-parse --short HEAD", { cwd: REPO }).toString().trim(); } catch {}

// npm-published core version (the tarball on npm) is NOT necessarily the repo version;
// stated explicitly so "published" vs "on main / pending publish" never blur.
const state = {
  repo_version: rootPkg.version,
  attest_core_version: corePkg.version,
  npm_core_published_version: "2.1.0", // update ONLY when a new version is actually published to npm
  attest_core_pending_publish: corePkg.version !== "2.1.0",
  tests_passed,
  tests_total,
  bundle_version: 1,
  mcp_tools: mcpTools,
  commit,
  generated_note: "Generated by scripts/gen-release-state.mjs. All public surfaces (README, SKILL.md, server.json, registry, demo badges) reconcile to THIS file.",
};

const outPath = R("release-state.json");
const next = JSON.stringify(state, null, 2) + "\n";

if (process.argv.includes("--check")) {
  const cur = (() => { try { return readFileSync(outPath, "utf8"); } catch { return ""; } })();
  // ignore the commit line when checking drift (it changes every commit)
  const strip = (s) => s.replace(/"commit":\s*"[^"]*"/, '"commit":""');
  if (strip(cur) !== strip(next)) {
    console.error("release-state.json is stale — run: node scripts/gen-release-state.mjs");
    process.exit(1);
  }
  console.log("release-state.json: up to date");
} else {
  writeFileSync(outPath, next);
  console.log("wrote release-state.json:", JSON.stringify({ repo: state.repo_version, core: state.attest_core_version, tests: `${tests_passed}/${tests_total}`, tools: mcpTools, commit }));
}
