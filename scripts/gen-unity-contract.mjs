// scripts/gen-unity-contract.mjs
// Single source of truth for the Unity spatial-agent contract: emits a generated C# file from
// the SHARED web registries (client-actions.mjs + types.ts), so the Unity client never defines a
// second copy of the action schema / state set / status set. The drift test regenerates + diffs.
//   node scripts/gen-unity-contract.mjs         # write the .g.cs
//   node scripts/gen-unity-contract.mjs --check  # exit 1 if out of date (drift)
import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";
import { CLIENT_ACTIONS } from "../apps/shadow-lens/web/spatial-agent/client-actions.mjs";

const ROOT = join(dirname(fileURLToPath(import.meta.url)), "..");
const TYPES = join(ROOT, "apps/shadow-lens/web/spatial-agent/src/app/types.ts");
const OUT = join(ROOT, "apps/shadow-lens/unity/Assets/ShadowLens/SpatialAgent/ShadowSpatialContract.g.cs");

// pull the value strings out of a `const X = { KEY: "value", ... } as const;` block in types.ts
function values(constName) {
  const src = readFileSync(TYPES, "utf8");
  const m = src.match(new RegExp(`const ${constName} = \\{([\\s\\S]*?)\\} as const`));
  if (!m) throw new Error(`${constName} not found in types.ts`);
  return [...m[1].matchAll(/:\s*"([^"]+)"/g)].map((x) => x[1]);
}

export function render() {
  const actions = Object.keys(CLIENT_ACTIONS);
  const argKeys = Object.fromEntries(actions.map((a) => [a, CLIENT_ACTIONS[a].id ?? null]));
  const states = values("FlowState");
  const statuses = values("ExecStatus");

  const lines = [];
  const L = (s = "") => lines.push(s);
  L("// <auto-generated> ShadowSpatialContract.g.cs — DO NOT EDIT.");
  L("// Generated from apps/shadow-lens/web/spatial-agent/{client-actions.mjs, src/app/types.ts}");
  L("// by scripts/gen-unity-contract.mjs. The Unity client shares ONE action/state/status schema");
  L("// with the tested web client; the drift test fails if this file is stale.");
  L("#if UNITY_2020_1_OR_NEWER");
  L("using System.Collections.Generic;");
  L("namespace ShadowLens.SpatialAgent {");
  L("  public static class ShadowSpatialContract {");
  L(`    public static readonly string[] Actions = { ${actions.map((a) => `"${a}"`).join(", ")} };`);
  L("    // action name -> required arg key (null = no arg)");
  L("    public static readonly Dictionary<string,string> ActionArg = new Dictionary<string,string> {");
  for (const a of actions) L(`      { "${a}", ${argKeys[a] ? `"${argKeys[a]}"` : "null"} },`);
  L("    };");
  L(`    public static readonly string[] States = { ${states.map((s) => `"${s}"`).join(", ")} };`);
  L(`    public static readonly string[] ExecStatuses = { ${statuses.map((s) => `"${s}"`).join(", ")} };`);
  L("  }");
  L("}");
  L("#endif");
  return lines.join("\n") + "\n";
}

if (import.meta.url === `file://${process.argv[1]}`) {
  const content = render();
  if (process.argv.includes("--check")) {
    const cur = (() => { try { return readFileSync(OUT, "utf8"); } catch { return ""; } })();
    if (cur !== content) { console.error("DRIFT: ShadowSpatialContract.g.cs is stale — run `node scripts/gen-unity-contract.mjs`"); process.exit(1); }
    console.log("ShadowSpatialContract.g.cs is up to date");
  } else {
    writeFileSync(OUT, content);
    console.log("wrote", OUT);
  }
}
