// Generate the offline token-review page from the canonical JSON. Self-contained (data inlined), no CDN, no
// external request, deterministic. Renders every semantic state grouped by category with EN/ZH labels, icon
// + shape names, semantic colour swatch, a11y text, and a profile selector (DesktopDark / OST Positive/
// Negative / AccessibilityHighContrast / GrayscaleReview) that re-shades WITHOUT changing meaning — the
// grayscale mode demonstrates that every state stays identifiable by shape + text alone.
//   node scripts/generate-token-review.mjs
import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const ROOT = join(dirname(fileURLToPath(import.meta.url)), "..");
const tok = JSON.parse(readFileSync(join(ROOT, "design", "shadow-spatial-tokens.json"), "utf8"));
const CATS = ["status", "governance", "trust_posture", "tracking", "interaction", "capability"];
const esc = (s) => String(s).replace(/&/g, "&amp;").replace(/</g, "&lt;").replace(/>/g, "&gt;");

const data = {};
for (const c of CATS) { data[c] = {}; for (const k of Object.keys(tok[c]).sort()) data[c][k] = tok[c][k]; }

const CONFLICT_PAIRS = [
  ["status.VERIFIED", "governance.APPROVAL_PRESENT"], ["status.VERIFIED", "governance.HUMAN_REVIEW_RECORDED"],
  ["status.FAILED", "status.DOWNSTREAM_AFFECTED"], ["status.FIRST_FAILURE", "status.DOWNSTREAM_AFFECTED"],
  ["status.WARNING", "tracking.SCANNING"], ["tracking.SCANNING", "tracking.LOST"],
  ["status.NOT_EVALUATED", "governance.APPROVAL_NOT_PRESENT"], ["trust_posture.SELF_SIGNED", "trust_posture.TIME_ANCHORED"],
  ["interaction.FOCUSED", "interaction.SELECTED"], ["capability.BUILT", "capability.DEVICE_VALIDATED"],
];

const html = `<!doctype html>
<meta charset="utf-8"><meta name="viewport" content="width=device-width,initial-scale=1">
<title>Shadow — design token review</title>
<style>
  :root { color-scheme: dark; }
  body { margin:0; font:14px/1.5 ui-sans-serif,system-ui,"PingFang SC",sans-serif; background:#0b0f16; color:#e8ecf2; }
  header { padding:16px 24px; background:#111826; border-bottom:1px solid #202a3a; position:sticky; top:0; }
  .badge { background:#961418; color:#fff; font-weight:800; padding:3px 10px; border-radius:4px; font-size:12px; }
  h1 { font-size:18px; margin:8px 0 0; } .sub { color:#9aa6b5; font-size:12px; }
  .controls { margin-top:10px; } select { background:#0f131b; color:#e8ecf2; border:1px solid #2a3550; border-radius:6px; padding:4px 8px; }
  main { padding:24px; } h2 { font-size:14px; letter-spacing:1px; text-transform:uppercase; color:#9aa6b5; border-bottom:1px solid #202a3a; padding-bottom:6px; margin-top:32px; }
  .grid { display:grid; grid-template-columns:repeat(auto-fill,minmax(300px,1fr)); gap:12px; margin-top:12px; }
  .card { background:#111826; border:1px solid #202a3a; border-radius:10px; padding:12px; }
  .row1 { display:flex; align-items:center; gap:10px; }
  .swatch { width:34px; height:34px; border-radius:7px; border:2px solid rgba(255,255,255,.18); flex:0 0 auto; }
  .key { font-weight:800; } .zh { color:#c4cdd9; } .meta { color:#8a93a3; font-size:12px; margin-top:6px; }
  .glyphs { font-size:12px; color:#9aa6b5; margin-top:4px; }
  .grayscale main { filter: grayscale(1); }
  .conflicts { margin-top:8px; } .pair { display:inline-flex; gap:6px; align-items:center; background:#0f131b; border:1px solid #2a3550; border-radius:20px; padding:3px 10px; margin:3px; font-size:12px; }
  .dot { width:12px;height:12px;border-radius:50%; display:inline-block; }
</style>
<header>
  <span class="badge">DESIGN TOKEN REVIEW — NOT DEVICE VALIDATED</span>
  <h1>Shadow semantic tokens</h1>
  <div class="sub">canonical source: design/shadow-spatial-tokens.json (${tok.version}) · offline · no CDN · generated by scripts/generate-token-review.mjs</div>
  <div class="controls">profile:
    <select id="profile" onchange="document.body.className=this.value">
      <option value="">DesktopDark (semantic identity)</option>
      <option value="grayscale">GrayscaleReview — must stay identifiable by shape + text</option>
    </select>
  </div>
</header>
<main>
  <section>
    <h2>Distinctness pairs — these must never look the same</h2>
    <div class="conflicts" id="conflicts"></div>
  </section>
  <div id="cats"></div>
</main>
<script>
  const DATA = ${JSON.stringify(data)};
  const PAIRS = ${JSON.stringify(CONFLICT_PAIRS)};
  const get = id => { const [c,k]=id.split("."); return DATA[c][k]; };
  const el = (t,c,h) => { const e=document.createElement(t); if(c)e.className=c; if(h!=null)e.textContent=h; return e; };
  // conflict pills
  const cwrap = document.getElementById("conflicts");
  for (const [a,b] of PAIRS) {
    const A=get(a), B=get(b);
    const p = el("span","pair");
    p.append(Object.assign(el("span","dot"),{style:"background:"+A.color}), el("span",null,A.text), el("span",null,"≠"),
             Object.assign(el("span","dot"),{style:"background:"+B.color}), el("span",null,B.text));
    cwrap.append(p);
  }
  // categories
  const cats = document.getElementById("cats");
  for (const c of Object.keys(DATA)) {
    cats.append(el("h2",null,c.replace(/_/g," ")));
    const g = el("div","grid");
    for (const k of Object.keys(DATA[c])) {
      const t = DATA[c][k]; const card = el("div","card");
      const r1 = el("div","row1");
      r1.append(Object.assign(el("span","swatch"),{style:"background:"+t.color}));
      const nm = el("div"); nm.append(Object.assign(el("div","key"),{textContent:t.text}), Object.assign(el("div","zh"),{textContent:t.text_zh}));
      r1.append(nm);
      card.append(r1);
      card.append(Object.assign(el("div","glyphs"),{textContent:"icon: "+t.icon+" · shape: "+t.shape+" · "+t.color}));
      card.append(Object.assign(el("div","meta"),{textContent:t.a11y}));
      card.append(Object.assign(el("div","meta"),{textContent:t.a11y_zh}));
      g.append(card);
    }
    cats.append(g);
  }
</script>`;

writeFileSync(join(ROOT, "reports/spatial-ux-v11/token-review/index.html"), html);
console.log("wrote reports/spatial-ux-v11/token-review/index.html (offline, self-contained)");
